n1 = input('Digite um valor: ')
print(n1.isnumeric()) #Ele confirma se o que foi digitado é número ou não, sendo que só pode ter número.

n2 = (input('Digite um valor: '))
print(n2.isupper()) #Ele confirma se o que foi digitado está em somente letra MAIÚSCULA ou não, só pode letra.

n3 = (input('Digite um valor: '))
print(n3.islower()) #Ele confirma se o que foi digitado está em somente letra minúscula ou não, só pode letra.

n4 = (input('Digite um valor: '))
print(n4.isalnum()) #Ele confirma se o que foi digitado é número ou não, sendo que só pode ter número.

n5 = (input('Digite um valor: '))
print(n5.isalpha()) #Ele confirma se o que foi digitado é alfabético ou não, sendo que só pode ter letra.

n6 = (input('Digite um valor: '))
print(n6.isdigit()) #Ele confirma se o que foi digitado é um número de 0 a 9, sem sinais ou pontos decimais.

n7 = (input('Digite um valor: '))
print(n7.isdecimal()) #Ele confirma se o que foi digitado é um número decimal, ou seja, pode ter dígitos de 0 a 9, mas sem sinais ou pontos.

n8 = (input('Digite um valor: '))
print(n8.isidentifier()) #Ele confirma se o que foi digitado pode ser usado como um identificador válido em Python, ou seja, deve começar com uma letra ou sublinhado (_) e não pode conter espaços ou caracteres especiais.

n9 = (input('Digite um valor: '))
print(n9.isprintable()) #Ele confirma se o que foi digitado pode ser impresso, servindo tanto para letras quanto para números e símbolos, exceto caracteres de controle.

n10 = (input('Digite um valor: '))
print(n10.isspace()) #Ele confirma se o que foi digitado é composto apenas por espaços, tabulações ou quebras de linha.

n11 = (input('Digite um valor: '))
print(n11.istitle()) #Ele confirma se o que foi digitado está no formato de título, ou seja, cada palavra começa com letra maiúscula e o restante em minúscula.




